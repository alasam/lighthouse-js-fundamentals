const isEven  = function (num) {
  return num % 2 === 0; // checks to see if number can be divided by 2 without having remainder. If remainder is equal to 0, it is true.
}

console.log(isEven(10));  // calls isEven function expression, should assign True to variable
console.log(isEven(11));  // calls isEven function expression, should assign False to variabl
